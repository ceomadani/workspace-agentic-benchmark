LANGUAGE_NAMES = {
    "en": "English",
    "it": "Italiano",
    "fr": "Français",
    "es": "Español",
    "de": "Deutsch",
    "pt": "Português",
}


IGNORE_DIRS = {".git", "node_modules", "__pycache__", ".venv", "venv", ".next", "dist", "build", ".pytest_cache"}


def detect_language(workspace: Path, sample_files: int = 50, sample_bytes: int = 5000) -> tuple[str, dict[str, int]]:
    """
    Detect dominant language in a workspace.

    Returns: (language_code, score_distribution)
    """
    scores: Counter = Counter()
    sampled = 0

    for dirpath, dirnames, filenames in os.walk(workspace):
        dirnames[:] = [d for d in dirnames if d not in IGNORE_DIRS and not d.startswith(".") or d in {".claude", ".github"}]
        for fname in filenames:
            if sampled >= sample_files:
                break
            if not fname.endswith((".md", ".txt", ".rst")):
                continue
            fp = Path(dirpath) / fname
            try:
                if fp.stat().st_size > 1_000_000:
                    continue
                content = fp.read_text(encoding="utf-8", errors="ignore")[:sample_bytes].lower()
            except (OSError, UnicodeDecodeError):
                continue
            # Quickly tokenize
            tokens = re.findall(r"\b[a-zA-ZàáâèéêìíîòóôùúûäëïöüßçñõÀ-ÿ]+\b", content)
            if len(tokens) < 20:
                continue
            for lang_code, markers in LANGUAGE_MARKERS.items():
                marker_set = set(markers)
                scores[lang_code] += sum(1 for t in tokens if t in marker_set)
            sampled += 1
        if sampled >= sample_files:
            break

    if not scores:
        return "en", {}

    # Largest wins · fall back to en if tie or very low signal
    dominant, top_score = scores.most_common(1)[0]
    if top_score < 5:
        return "en", dict(scores)
    return dominant, dict(scores)


def t(key: str, lang: str = "en") -> str:
    """Translate a key. Falls back to en if missing in target language."""
    lang_dict = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    if key in lang_dict:
        return lang_dict[key]
    return TRANSLATIONS["en"].get(key, key)


def language_name(lang: str) -> str:
    return LANGUAGE_NAMES.get(lang, lang)
