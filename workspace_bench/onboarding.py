"""
Interactive onboarding wizard · Rich-prompts UI · multi-folder support.

Workflow:
  1. Welcome banner
  2. Ask workspace path(s) — single folder or multi-folder list
  3. Detect language · stack · size · sniff config files
  4. Suggest detected configuration
  5. Save to .workspace-bench.yaml in cwd
  6. Show next steps

Output: .workspace-bench.yaml that `workspace-bench run` will auto-load.
"""

from __future__ import annotations
import os
from pathlib import Path
from typing import Any

from rich.console import Console
from rich.prompt import Prompt, Confirm, IntPrompt
from rich.panel import Panel
from rich.text import Text
from rich.table import Table
from rich.box import HEAVY, ROUNDED

from .i18n import detect_language, language_name
from .audit import workspace_stats, fmt_size


CONFIG_FILENAME = ".workspace-bench.yaml"


def detect_stack(workspace: Path) -> list[str]:
    """Sniff workspace for known stack markers."""
    stack: list[str] = []
    checks = {
        "Python": ["pyproject.toml", "requirements.txt", "setup.py", "Pipfile"],
        "Node.js": ["package.json"],
        "Next.js": ["next.config.js", "next.config.mjs", "next.config.ts"],
        "TypeScript": ["tsconfig.json"],
        "Rust": ["Cargo.toml"],
        "Go": ["go.mod"],
        "Ruby": ["Gemfile"],
        "PHP": ["composer.json"],
        "Java": ["pom.xml", "build.gradle"],
        "Docker": ["Dockerfile", "docker-compose.yml", "docker-compose.yaml"],
        "Terraform": ["main.tf"],
        "GitHub Actions": [".github/workflows"],
        "Claude Code": [".claude", "CLAUDE.md"],
        "Cursor": [".cursorrules", ".cursor"],
        "n8n": ["n8n", ".n8n"],
        "Supabase": ["supabase", "supabase.config.json"],
        "Vercel": ["vercel.json", ".vercel"],
    }
    for tech, markers in checks.items():
        for marker in markers:
            target = workspace / marker
            if target.exists():
                stack.append(tech)
                break
    return stack


def write_config(config: dict, output_path: Path) -> None:
    """Write a minimal YAML config (no PyYAML dependency)."""
    lines = ["# workspace-bench configuration · generated by `workspace-bench init`", ""]
    lines.append("version: 1")
    lines.append("")
    lines.append("workspace:")
    if isinstance(config["workspace"], list):
        lines.append("  paths:")
        for p in config["workspace"]:
            lines.append(f"    - {p}")
    else:
        lines.append(f"  path: {config['workspace']}")
    lines.append("")
    lines.append(f"workspace_name: \"{config.get('workspace_name', '')}\"")
    lines.append(f"language: {config.get('language', 'en')}")
    lines.append("")
    if config.get("stack"):
        lines.append("stack:")
        for s in config["stack"]:
            lines.append(f"  - {s}")
        lines.append("")
    lines.append(f"domain: \"{config.get('domain', '')}\"")
    lines.append(f"team_size: {config.get('team_size', 1)}")
    lines.append("")
    if "output_dir" in config:
        lines.append(f"output_dir: {config['output_dir']}")
        lines.append("")
    if "weights" in config:
        lines.append("weights:")
        for k, v in config["weights"].items():
            lines.append(f"  {k}: {v}")
        lines.append("")
    lines.append("# Run audit with this config:")
    lines.append("#   workspace-bench run")
    lines.append("")
    output_path.write_text("\n".join(lines), encoding="utf-8")


def read_config(config_path: Path) -> dict:
    """Read minimal YAML config (no PyYAML dependency)."""
    if not config_path.exists():
        return {}
    config: dict[str, Any] = {}
    current_list_key = None
    current_dict_key = None
    for raw_line in config_path.read_text(encoding="utf-8").splitlines():
        line = raw_line.rstrip()
        if not line or line.lstrip().startswith("#"):
            continue
        if line.startswith("  - ") and current_list_key:
            config[current_list_key].append(line[4:].strip().strip('"'))
            continue
        if line.startswith("  ") and current_dict_key and ":" in line:
            k, _, v = line.strip().partition(":")
            config[current_dict_key][k.strip()] = float(v.strip()) if v.strip() and _is_number(v.strip()) else v.strip().strip('"')
            continue
        current_list_key = None
        current_dict_key = None
        if ":" not in line:
            continue
        key, _, value = line.partition(":")
        key = key.strip()
        value = value.strip().strip('"')
        if value == "":
            # could be a list or dict header
            if key in ("stack", "paths"):
                config[key] = []
                current_list_key = key
            elif key in ("weights",):
                config[key] = {}
                current_dict_key = key
            elif key == "workspace":
                pass  # nested, skip header
        else:
            if _is_number(value):
                config[key] = float(value) if "." in value else int(value)
            else:
                config[key] = value
    # Normalize 'paths' or 'path' nested in workspace section
    if "paths" in config:
        config["workspace"] = config["paths"]
        del config["paths"]
    if "path" in config:
        config["workspace"] = config["path"]
        del config["path"]
    return config


def _is_number(s: str) -> bool:
    try:
        float(s)
        return True
    except ValueError:
        return False


def welcome_panel(version: str) -> Panel:
    body = Text()
    body.append("\n  ")
    body.append("Welcome to the Workspace Agentic Benchmark", style="bold white")
    body.append("\n  ")
    body.append("This wizard will:", style="dim")
    body.append("\n  ", style="dim")
    body.append("· ask where your workspace lives (one folder or multiple)\n  ", style="cyan")
    body.append("· detect language · stack · size automatically\n  ", style="cyan")
    body.append("· save a ", style="cyan")
    body.append(".workspace-bench.yaml", style="bold cyan")
    body.append(" config so `workspace-bench run` works out of the box\n  ", style="cyan")
    body.append("· prepare the audit pipeline\n", style="cyan")
    return Panel(body, title=f"[bold cyan]┃ workspace-bench v{version}  ·  ONBOARDING", title_align="left", border_style="cyan", box=HEAVY, padding=(0, 1))


def detected_panel(workspace_paths: list[Path], lang: str, stats: dict, stack: list[str]) -> Panel:
    body = Text()
    body.append("\n")
    if len(workspace_paths) == 1:
        body.append("  Path           ", style="dim")
        body.append(str(workspace_paths[0]), style="bold cyan")
    else:
        body.append("  Paths\n", style="dim")
        for p in workspace_paths:
            body.append("    - ", style="grey50")
            body.append(str(p), style="cyan")
            body.append("\n")
    body.append("\n  Files          ", style="dim")
    body.append(f"{stats['files']:,}", style="cyan")
    body.append("    ", style="dim")
    body.append("Size  ", style="dim")
    body.append(fmt_size(stats["bytes"]), style="cyan")
    body.append("\n  Language       ", style="dim")
    body.append(f"{language_name(lang)} ({lang})", style="cyan")
    body.append("\n  Stack          ", style="dim")
    if stack:
        body.append(" · ".join(stack), style="cyan")
    else:
        body.append("(unknown · skip stack tagging)", style="dim italic")
    body.append("\n")
    return Panel(body, title="[bold cyan]┃ DETECTED", title_align="left", border_style="dim", box=ROUNDED, padding=(0, 1))


def run_init(console: Console, version: str, force: bool = False) -> None:
    cwd = Path.cwd()
    config_path = cwd / CONFIG_FILENAME

    if config_path.exists() and not force:
        console.print()
        console.print(Panel(
            Text(f"\n  A config already exists at {config_path}\n  Re-run with --force to overwrite.\n", style="yellow"),
            border_style="yellow", box=ROUNDED,
        ))
        return

    console.print()
    console.print(welcome_panel(version))
    console.print()

    # ─── Step 1 · workspace path(s) ────────────────────────────────────────
    multi = Confirm.ask(
        "[cyan]Is your workspace a single folder?[/cyan]",
        default=True,
        console=console,
    )

    workspace_paths: list[Path] = []
    if multi:
        path_str = Prompt.ask(
            "[cyan]Workspace path[/cyan]",
            default=str(cwd),
            console=console,
        )
        wp = Path(path_str).expanduser().resolve()
        if not wp.exists() or not wp.is_dir():
            console.print(f"[red]✗ Path does not exist or is not a directory:[/red] {wp}")
            return
        workspace_paths.append(wp)
    else:
        console.print("[dim]Enter paths one per line. Empty line to finish.[/dim]")
        while True:
            path_str = Prompt.ask("[cyan]Path[/cyan]", default="", console=console)
            if not path_str.strip():
                if workspace_paths:
                    break
                console.print("[red]At least one path required.[/red]")
                continue
            wp = Path(path_str).expanduser().resolve()
            if not wp.exists() or not wp.is_dir():
                console.print(f"[red]✗ Path does not exist:[/red] {wp}")
                continue
            workspace_paths.append(wp)
            console.print(f"[green]  ✓ Added[/green] {wp}")

    # ─── Step 2 · detect language · stats · stack ─────────────────────────
    primary_path = workspace_paths[0]
    console.print()
    console.print("[dim]  Detecting language · stack · size...[/dim]")
    lang, _ = detect_language(primary_path)
    stats = workspace_stats(primary_path)
    if len(workspace_paths) > 1:
        for extra in workspace_paths[1:]:
            extra_stats = workspace_stats(extra)
            stats["files"] += extra_stats["files"]
            stats["dirs"] += extra_stats["dirs"]
            stats["bytes"] += extra_stats["bytes"]
    stack = detect_stack(primary_path)
    console.print()
    console.print(detected_panel(workspace_paths, lang, stats, stack))
    console.print()

    # ─── Step 3 · confirm language or override ────────────────────────────
    lang_override = Prompt.ask(
        f"[cyan]Report language?[/cyan] [dim](detected: {language_name(lang)})[/dim]",
        choices=["en", "it", "fr", "es", "de", "pt"],
        default=lang,
        console=console,
    )

    # ─── Step 4 · workspace name + domain ─────────────────────────────────
    default_name = primary_path.name
    workspace_name = Prompt.ask(
        "[cyan]Workspace name[/cyan]",
        default=default_name,
        console=console,
    )
    domain = Prompt.ask(
        "[cyan]Domain[/cyan]",
        choices=["financial-services", "healthcare", "ecommerce", "saas", "public-sector", "personal", "research", "other"],
        default="other",
        console=console,
    )
    team_size = IntPrompt.ask(
        "[cyan]Team size[/cyan] [dim](1 solo · 2-10 small · 11-50 medium · 51+ large)[/dim]",
        default=1,
        console=console,
    )

    # ─── Step 5 · output dir ──────────────────────────────────────────────
    output_dir = Prompt.ask(
        "[cyan]Output directory for audit artifacts[/cyan]",
        default="./bench-output",
        console=console,
    )

    # ─── Save config ──────────────────────────────────────────────────────
    config = {
        "workspace": [str(p) for p in workspace_paths] if len(workspace_paths) > 1 else str(workspace_paths[0]),
        "workspace_name": workspace_name,
        "language": lang_override,
        "stack": stack,
        "domain": domain,
        "team_size": team_size,
        "output_dir": output_dir,
    }
    write_config(config, config_path)

    # ─── Summary ──────────────────────────────────────────────────────────
    console.print()
    summary = Text()
    summary.append("\n  ✓ ", style="bold bright_green")
    summary.append("Config saved to ", style="bold")
    summary.append(str(config_path), style="cyan")
    summary.append("\n\n  Next steps:\n", style="dim")
    summary.append("    workspace-bench run", style="bold yellow")
    summary.append("                              ", style="dim")
    summary.append("# audit + score + HTML report (uses config)\n", style="dim italic")
    summary.append("    workspace-bench audit ", style="bold yellow")
    summary.append(str(workspace_paths[0]), style="yellow")
    summary.append("       ", style="dim")
    summary.append("# audit only\n", style="dim italic")
    summary.append("\n  Tip: edit ", style="dim")
    summary.append(str(config_path), style="cyan")
    summary.append(" to customize weights, language, etc.\n", style="dim")
    console.print(Panel(summary, title="[bold cyan]┃ ONBOARDING COMPLETE", title_align="left", border_style="bright_green", box=ROUNDED, padding=(0, 1)))
    console.print()
